package menu_lenh;

public class Dientich_hinhtron {
    public static final float PI = 3.14f;

    //method tinh dien tich hinh tron
    public float chuvihinhtron(float bankinh){
        return 2*bankinh*PI;
    }

    // method tinh dien tich hinh tron

    public float dientichhinhtron(float bankinh){
        return PI*bankinh*bankinh;
    }
}
