package list_keyword;
/* cùng tiềm hiểu về từ khóa keyword Final() trong java*/

// class final
final class A{
    void display(){
        System.out.print("giá trị lớp A có Final là không thể từa ế nha");
    }
}

public class Final_in_java {
    public static  void main(String[] args){
        // khái báo final cho variable
        final double PI = 3.1435;
         System.out.println("giá trị PI: " + PI);
         //--.> kw: dù có gáng giá trị nào thì Pi vẫn bằng giá trị khởi tạo ban đầuvowisis final
    }
}
