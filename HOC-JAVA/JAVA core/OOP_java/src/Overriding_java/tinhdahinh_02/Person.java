package Overriding_java.tinhdahinh_02;

public class Person {
    private String name;
    private int Age;

    // get and set

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int age) {
        Age = age;
    }
}
